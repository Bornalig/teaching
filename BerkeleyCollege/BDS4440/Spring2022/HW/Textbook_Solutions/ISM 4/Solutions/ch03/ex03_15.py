# Exercise 3.15
"""Approximate e."""
number = 1
factorial = 1
e = 1.0

accuracy = int(input('Enter desired accuracy of e: '))

while number < accuracy:
    factorial *= number
    e += 1.0 / factorial
    number += 1

print(f'e is {e}')



